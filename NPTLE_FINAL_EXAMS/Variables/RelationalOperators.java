public class RelationalOperators {
    public static void main(String[] args) {
        int a = 3;
        int b = 5;
        System.out.println("Are equal to " + (a == b));
        System.out.println("Are not equal to " + (a != b));
        System.out.println("Are a is grator to " + (a > b));
        System.out.println("Are b is grator to " + (a < b));
        System.out.println("Are a is grator then equal to " + (a >= b));
        System.out.println("Are a is less then equal to " + (a <= b));
    }
}