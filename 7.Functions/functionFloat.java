public class functionFloat {
    public static int sum(int a, int b ){
        return a + b;
    }
    public static float sum(float a, float b ){
        return a + b;
    }
    public static void main(String[] args) {
        System.out.println(sum(9.7f, 3.8f));
        System.out.println(sum(5, 6));
    }
}
